SET FOREIGN_KEY_CHECKS=0;

# 附件信息表
DROP TABLE IF EXISTS `b_attached`;
CREATE TABLE `b_attached` (
  `id` INT(11) UNSIGNED NOT NULL AUTO_INCREMENT COMMENT '编号',
  `bucket` VARCHAR(20) NOT NULL DEFAULT '' COMMENT '目录名',
  `object` VARCHAR(50) NOT NULL DEFAULT '' COMMENT '文件名',
  `link` VARCHAR(200) DEFAULT NULL COMMENT '外链',
  `mime` TINYINT(3) DEFAULT NULL COMMENT '类型',
  `size` INT(11) UNSIGNED NOT NULL COMMENT '文件大小',
  `addtime` INT(10) UNSIGNED NOT NULL COMMENT '添加时间',
  `state` TINYINT(1) UNSIGNED NOT NULL DEFAULT '1' COMMENT '状态',
  PRIMARY KEY (`id`)
)  ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='附件信息表';

# 附件类型表
DROP TABLE IF EXISTS `b_attached_mime`;
CREATE TABLE `b_attached_mime` (
  `id` TINYINT(3) UNSIGNED NOT NULL AUTO_INCREMENT COMMENT '编号',
  `name` VARCHAR(50) NOT NULL DEFAULT '' COMMENT '类型名称',
  `sort` TINYINT(3) UNSIGNED NOT NULL DEFAULT '0' COMMENT '排序',
  PRIMARY KEY (`id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='附件类型表';